import { readdirSync, readFileSync, writeFileSync } from "node:fs";
import { basename, join, resolve } from "node:path";

const packageRoot = process.cwd();
const svgDir = resolve(
	packageRoot,
	process.argv[2] ?? "src/components/icons/svg",
);
const outputFile = resolve(
	packageRoot,
	"src/components/icons/rounded-icon-data.generated.ts",
);

const aliasTargets = {
	bell: "automations",
	browser: "globe",
	files: "folders",
	moon: "gpu",
	question: "question-circle",
	"settings-cog": "settings",
	"sidebar-closed": "sidebar",
	three: "more",
};

function readAttribute(attributes, name) {
	const match = attributes.match(new RegExp(`\\b${name}="([^"]*)"`));
	return match ? match[1] : "";
}

function svgInner(svg) {
	return svg
		.replace(/^<svg\b[^>]*>/s, "")
		.replace(/<\/svg>\s*$/s, "")
		.trim();
}

function svgPresentationInner(svg, attributes) {
	const inner = svgInner(svg);
	const presentationAttributeNames = [
		"fill",
		"stroke",
		"stroke-width",
		"stroke-linecap",
		"stroke-linejoin",
	];
	const presentationAttributes = presentationAttributeNames
		.map((name) => {
			const value = readAttribute(attributes, name);
			return value ? `${name}=${JSON.stringify(value)}` : null;
		})
		.filter((attribute) => attribute !== null)
		.join(" ");

	return presentationAttributes ? `<g ${presentationAttributes}>${inner}</g>` : inner;
}

function quoted(value) {
	return JSON.stringify(value);
}

function readSvgEntry(fileName) {
	const name = basename(fileName, ".svg");
	if (!/^[a-z0-9]+(?:-[a-z0-9]+)*$/.test(name)) {
		throw new Error(`Icon file must use a clean kebab-case name: ${fileName}`);
	}

	const svg = readFileSync(join(svgDir, fileName), "utf8").trim();
	const match = svg.match(/^<svg\b([^>]*)>/s);
	if (!match) {
		throw new Error(`Missing <svg> wrapper in ${fileName}`);
	}

	const viewBox = readAttribute(match[1], "viewBox");
	if (!viewBox) {
		throw new Error(`Missing viewBox in ${fileName}`);
	}

	return {
		name,
		viewBox,
		inner: svgPresentationInner(svg, match[1]),
	};
}

const sourceFiles = readdirSync(svgDir)
	.filter((fileName) => fileName.endsWith(".svg"))
	.sort();

const entries = sourceFiles.map((fileName) => readSvgEntry(fileName));
const iconNames = entries.map((entry) => entry.name);
const iconNameSet = new Set(iconNames);

if (iconNameSet.size !== iconNames.length) {
	throw new Error("Duplicate rounded icon names found");
}

for (const [aliasName, targetName] of Object.entries(aliasTargets)) {
	if (iconNameSet.has(aliasName)) {
		throw new Error(`Alias ${aliasName} conflicts with a primary icon`);
	}

	if (!iconNameSet.has(targetName)) {
		throw new Error(`Alias ${aliasName} points to missing icon ${targetName}`);
	}
}

const aliasNames = Object.keys(aliasTargets).sort();
const iconNameLines = iconNames.map((name) => `\t${quoted(name)},`);
const aliasNameLines = aliasNames.map((name) => `\t${quoted(name)},`);
const iconDataLines = entries.map(
	(entry) =>
		`\t${quoted(entry.name)}: { viewBox: ${quoted(entry.viewBox)}, inner: ${quoted(entry.inner)} },`,
);
const aliasLines = aliasNames.map(
	(name) => `\t${quoted(name)}: ${quoted(aliasTargets[name])},`,
);

const output = `// Generated by packages/ui/scripts/generate-rounded-icons.mjs.
// Source: packages/ui/src/components/icons/svg (${sourceFiles.length} SVG files).

export type RoundedIconData = {
\treadonly viewBox: string;
\treadonly inner: string;
};

export const roundedIconNames = [
${iconNameLines.join("\n")}
] as const;

export const roundedIconSourceNames = roundedIconNames;

export type RoundedIconSourceName = (typeof roundedIconNames)[number];

/**
 * @deprecated Use RoundedIconSourceName. These ids are clean SVG source names,
 * not a separate canonical namespace.
 */
export type RoundedIconCanonicalName = RoundedIconSourceName;

export const roundedIconData: Record<RoundedIconSourceName, RoundedIconData> = {
${iconDataLines.join("\n")}
};

export const roundedIconAliasNames = [
${aliasNameLines.join("\n")}
] as const;

export type RoundedIconAliasName = (typeof roundedIconAliasNames)[number];

export const roundedIconAliases: Record<RoundedIconAliasName, RoundedIconSourceName> = {
${aliasLines.join("\n")}
};

export type RoundedIconName = RoundedIconSourceName | RoundedIconAliasName;

export function isRoundedIconAliasName(name: RoundedIconName): name is RoundedIconAliasName {
\treturn Object.hasOwn(roundedIconAliases, name);
}

export function resolveRoundedIconName(name: RoundedIconName): RoundedIconSourceName {
\tif (isRoundedIconAliasName(name)) {
\t\treturn roundedIconAliases[name];
\t}

\treturn name;
}
`;

writeFileSync(outputFile, output);

console.log(
	`Generated ${entries.length} rounded icons and ${aliasNames.length} aliases at ${outputFile}`,
);
