/**
 * Get glossary context for a specific language
 */
export function getGlossaryForLanguage(langCode: string): string {
	const terms: string[] = [];

	for (const [english, translations] of Object.entries(GLOSSARY)) {
		const translation = translations[langCode];
		if (translation) {
			terms.push(`- "${english}" → "${translation}"`);
		}
	}

	return terms.length > 0 ? terms.join("\n") : "";
}

/**
 * Get the human-readable name for a language code
 */
export function getLanguageName(langCode: string): string {
	return LANGUAGE_NAMES[langCode] ?? langCode.toUpperCase();
}
